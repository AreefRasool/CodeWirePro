    # ------------------------------------------------------
    # VOLTAGE ANALYSIS FUNCTION
    # ------------------------------------------------------

    def voltage_analysis(voltage, current, length, resistance):

        if voltage <= 0:
            return (
                "<h2>Invalid Voltage</h2>",
                create_health_gauge(0),
                "0",
                "FAIL",
                "-",
                generate_kpi_cards(0, "FAIL", "-", "0%")
            )

        drop = (2 * current * length * resistance) / 1000
        percent = (drop / voltage) * 100
        score, status = calculate_score(percent)

        if current <= 10:
            wire = "14 AWG"
        elif current <= 16:
            wire = "12 AWG"
        elif current <= 25:
            wire = "10 AWG"
        elif current <= 35:
            wire = "8 AWG"
        else:
            wire = "6 AWG"

        report = f"""

        <div class='section-card'>
            <h2>⚡ Voltage Analysis</h2>
            <p>Voltage Drop: {drop:.2f} V</p>
            <p>Loss: {percent:.2f} %</p>
            <p>Status: {status}</p>
        </div>

        """

        kpi_html = generate_kpi_cards(score, status, wire, f"{percent:.2f}%")

        return (report, create_health_gauge(score), str(score), status, wire, kpi_html)

