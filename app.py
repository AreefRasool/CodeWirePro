# ==========================================================
# HEALTH SCORE ENGINE
# ==========================================================

def calculate_score(voltage_drop_percent):

    score = max(
        0,
        min(
            100,
            100 - (voltage_drop_percent * 8)
        )
    )

    if score >= 90:
        status = "PASS"
    elif score >= 70:
        status = "WARNING"
    else:
        status = "FAIL"

    return round(score), status

